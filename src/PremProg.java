public class PremProg {
    public static void main(String[] args) {
        System.out.println("Mon premier programme Java");

    }
}
/**
 * static : précise que la méthode main de la classe PremProg n'est pas liée à une instance (objet) particulière de la classe
        * main : c'est la fonction principale
        * String [] args : permet de récuperer des arguments transmis au programme au moment du lancement
        * public 'dans la class PremProg : sert à définir les droits d'accès des autres classes (en fait de leurs méthodes) à la classe PremProg
        * public dans public static main est obligatoire pour ue le programme puisse s'exécuter - la machine virtuelle accède ainsi à la méthode main
        * system désigne une classe dans lequel est définit le champ donnée out, représentant la fenetre console
        */